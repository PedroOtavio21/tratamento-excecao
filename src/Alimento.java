public class Alimento {
    // Atributos
    private int x;
    private int y;

    // Construtor
    public Alimento(int x, int y){
        this.x = x;
        this.y = y;
    }
    
    // Getters e Setters
    public int getX(){
        return this.x;
    }

    public void setX(int x){
        this.x = x;
    }

    public int getY(){
        return this.y;
    }

    public void setY(int y){
        this.y = y;
    }

}
